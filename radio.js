        function mudarCor(cor) {
            if (cor === "Amarelo") {
                document.body.style.backgroundColor = "yellow";
            } else if (cor === "Verde") {
                document.body.style.backgroundColor = "green";
            } else if (cor === "Rosa") {
                document.body.style.backgroundColor = "pink";
            } else {
                document.body.style.backgroundColor = "white";
            }
        }